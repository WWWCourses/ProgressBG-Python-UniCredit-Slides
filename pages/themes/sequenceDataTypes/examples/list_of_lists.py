matrix = [
    [1,2,3],
    [4,5,6],
    [7,8,9],
]

### retrieve the first element from the first list:
print(matrix[0][0] )
# 1

### retrieve the last element from the first list:
print(matrix[0][-1])
# 3

### retrieve the first element from the last list:
print(matrix[-1][0])
# 7

### retrieve the last element from the last list:
print(matrix[-1][-1])
# 9
